// 자기 이름 출력하는 프로그램
public class PrintName{
    public static void main(String[] args) {
        System.out.println("김정욱1");
        System.out.println("김정욱2");
        System.out.println("김정욱3");
        System.out.println("김정욱4");
        System.out.println("김정욱5");

        System.out.println("10"+"10"); // + 연산자(operator) 문자열
        System.out.println("11"+"11");
        System.out.println("12"+"12");

        System.out.println(20+20); // 정수
        System.out.println(21+21);
        System.out.println(22+22);
    }
}