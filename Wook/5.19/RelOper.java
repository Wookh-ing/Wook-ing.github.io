// 관계연산자

public class RelOper {
	public static void main(String[] args) {
		System.out.println(55 == 55); 
		System.out.println(55 != 55); // != 같지않다.
		System.out.println(55 > 55);  // 수학에서는 크다,작다 java에서는 크냐?작냐? 물음문
		System.out.println(55 < 55);
		System.out.println(55 >= 55);
		System.out.println(55 <= 55);
		
		
	}
}